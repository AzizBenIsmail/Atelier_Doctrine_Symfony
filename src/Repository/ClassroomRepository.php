    public function findStudentByClassAVG($name){
        $entityManager=$this->getEntityManager();
        $query=$entityManager
            ->createQuery("SELECT AVG(s.moyenne) as moyenne FROM APP\Entity\Student s JOIN s.classroom c WHERE c.name=:name ")
